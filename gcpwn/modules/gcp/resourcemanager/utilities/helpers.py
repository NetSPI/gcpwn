def build_hierarchy_tree(
    *,
    projects_resource: ResourceManagerProjectsResource,
    folders_resource: ResourceManagerFoldersResource,
    parent_ids: Iterable[str],
    debug: bool = False,
) -> dict[str, list[dict[str, Any]]]:
    pending_parents = deque(str(parent_id or "").strip() for parent_id in parent_ids or [])
    seen_parents: set[str] = set()
    discovered_folders: list[dict[str, Any]] = []
    discovered_projects: list[dict[str, Any]] = []

    while pending_parents:
        parent_name = str(pending_parents.popleft() or "").strip()
        if not parent_name or parent_name in seen_parents:
            continue
        seen_parents.add(parent_name)

        folders = folders_resource.list(parent=parent_name, debug=debug)
        if isinstance(folders, list) and folders:
            folders_resource.save(folders)
            for folder in folders:
                payload = resource_to_dict(folder)
                if not payload:
                    continue
                discovered_folders.append(payload)
                child_name = folders_resource.resource_name(payload)
                if child_name and child_name not in seen_parents:
                    pending_parents.append(child_name)

        projects = projects_resource.list(parent=parent_name, debug=debug)
        if isinstance(projects, list) and projects:
            projects_resource.save(projects)
            for project in projects:
                payload = resource_to_dict(project)
                if payload:
                    discovered_projects.append(payload)

    deduped_folders = _dedupe_rows(discovered_folders)
    deduped_projects = _dedupe_rows(discovered_projects)
    print(
        "[*] recursive list results: "
        f"folders discovered {len(deduped_folders)}, "
        f"projects discovered {len(deduped_projects)}"
    )
    return {
        "folders": deduped_folders,
        "projects": deduped_projects,
    }


def resolve_missing_hierarchy_parents(
    session,
    *,
    folders_resource: ResourceManagerFoldersResource,
    organizations_resource: ResourceManagerOrganizationsResource,
    debug: bool = False,
) -> dict[str, list[dict[str, Any]]]:
    try:
        rows = session.get_data(
            "abstract_tree_hierarchy",
            columns=["name", "display_name", "parent"],
        ) or []
    except Exception:
        rows = []

    normalized_rows = _dedupe_rows(rows)
    rows_by_name = {
        str(row.get("name") or "").strip(): dict(row)
        for row in normalized_rows
        if str(row.get("name") or "").strip()
    }
    folder_queue: deque[str] = deque()
    org_queue: deque[str] = deque()

    def _is_placeholder_label(resource_name: str, row: dict[str, Any] | None) -> bool:
        if not row:
            return True
        display_name = str(row.get("display_name") or "").strip()
        if not display_name:
            return True
        leaf = extract_path_tail(resource_name, default=resource_name)
        return display_name in {resource_name, leaf}

    def _enqueue_if_needed(resource_name: str) -> None:
        token = str(resource_name or "").strip()
        if not token:
            return
        row = rows_by_name.get(token)
        if not _is_placeholder_label(token, row):
            return
        if token.startswith("folders/"):
            folder_queue.append(token)
        elif token.startswith("organizations/"):
            org_queue.append(token)

    for row in normalized_rows:
        name = str(row.get("name") or "").strip()
        parent = str(row.get("parent") or "").strip()
        _enqueue_if_needed(name)
        if parent and parent not in {"N/A", "None"}:
            _enqueue_if_needed(parent)

    resolved_folders: list[dict[str, Any]] = []
    resolved_orgs: list[dict[str, Any]] = []
    seen_folders: set[str] = set()
    seen_orgs: set[str] = set()

    while folder_queue:
        folder_name = folder_queue.popleft()
        if folder_name in seen_folders:
            continue
        seen_folders.add(folder_name)
        folder = folders_resource.get(name=folder_name, debug=debug, quiet_not_found=True)
        if not isinstance(folder, dict) or not folder:
            continue
        folders_resource.save([folder])
        resolved_folders.append(folder)
        parent = str(folder.get("parent") or "").strip()
        if parent:
            _enqueue_if_needed(parent)

    while org_queue:
        org_name = org_queue.popleft()
        if org_name in seen_orgs:
            continue
        seen_orgs.add(org_name)
        org = organizations_resource.get(name=org_name, debug=debug, quiet_not_found=True)
        if not isinstance(org, dict) or not org:
            continue
        organizations_resource.save([org])
        resolved_orgs.append(org)

    print(
        "[*] get_* enrichment results: "
        f"folders resolved {len(resolved_folders)}/{len(seen_folders)}, "
        f"orgs resolved {len(resolved_orgs)}/{len(seen_orgs)}"
    )
    return {
        "folders": _dedupe_rows(resolved_folders),
        "organizations": _dedupe_rows(resolved_orgs),
    }
