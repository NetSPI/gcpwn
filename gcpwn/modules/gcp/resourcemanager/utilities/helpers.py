class _ResourceManagerBaseResource:
    TABLE_NAME = "abstract_tree_hierarchy"
    SERVICE_LABEL = "Resource Manager"
    RESOURCE_TYPE = ""
    SUMMARY_TYPE = ""
    ACTION_SCOPE_KEY = ""
    GENERAL_PERMISSION_FILE = ""
    ALL_PERMISSION_FILE = ""
    INVALID_PERMISSIONS: tuple[str, ...] = ()

    def __init__(self, session) -> None:
        self.session = session
        self.client = self._build_client()
        self._invalid_permissions: set[str] = set(self.INVALID_PERMISSIONS or ())

    def _build_client(self):
        raise NotImplementedError

    def _request(self, callback):
        return callback()

    def _save_one(self, row: Any) -> None:
        _persist_metadata_gcp(self.session, row, self.RESOURCE_TYPE)

    def save(self, rows: Iterable[Any]) -> None:
        for row in rows or []:
            self._save_one(row)

    def load_permissions(self, *, all_permissions: bool = False) -> list[str]:
        filename = self.ALL_PERMISSION_FILE if all_permissions else self.GENERAL_PERMISSION_FILE
        raw_permissions = read_lines(_permission_file(filename)) if filename else []
        all_permissions_set: set[str] = set()
        if self.ALL_PERMISSION_FILE:
            all_permissions_set = {
                str(permission or "").strip()
                for permission in read_lines(_permission_file(self.ALL_PERMISSION_FILE))
                if str(permission or "").strip()
            }
        deduped_permissions: list[str] = []
        seen: set[str] = set()
        for permission in raw_permissions:
            token = str(permission or "").strip()
            if not token or token in seen or token in self._invalid_permissions:
                continue
            if not all_permissions and all_permissions_set and token not in all_permissions_set:
                continue
            seen.add(token)
            deduped_permissions.append(token)
        return deduped_permissions

    def _register_invalid_permissions(self, error_text: str, batch_permissions: list[str]) -> set[str]:
        invalid_permissions = {
            str(match or "").strip()
            for match in INVALID_PERMISSION_RE.findall(str(error_text or ""))
            if str(match or "").strip()
        }
        if not invalid_permissions and "is not valid for this resource" in str(error_text or "") and len(batch_permissions) == 1:
            invalid_permissions = {str(batch_permissions[0] or "").strip()}
        self._invalid_permissions.update({permission for permission in invalid_permissions if permission})
        return invalid_permissions

    def resource_name(self, row: dict[str, Any] | Any) -> str:
        payload = resource_to_dict(row)
        return str(payload.get("name") or "").strip()

    def action_key(self, row: dict[str, Any] | Any) -> str:
        payload = resource_to_dict(row)
        return str(payload.get("name") or "").strip()

    def summary_rows(self, rows: Iterable[Any]) -> list[dict[str, Any]]:
        output: list[dict[str, Any]] = []
        for row in rows or []:
            payload = resource_to_dict(row)
            if not payload:
                continue
            output.append(
                {
                    "name": str(payload.get("name") or "").strip(),
                    "display_name": str(payload.get("display_name") or "").strip(),
                    "parent": str(payload.get("parent") or ""),
                    "project_id": str(payload.get("project_id") or ""),
                    "r_type": self.SUMMARY_TYPE,
                    "r_state": _resource_state_label(payload.get("state")),
                }
            )
        return output

    def record_permissions(self, action_dict: dict[str, Any], row: dict[str, Any] | Any, permissions: Iterable[str]) -> None:
        key = self.action_key(row)
        record_action_permissions(
            action_dict,
            permissions=permissions,
            scope_key=self.ACTION_SCOPE_KEY,
            scope_label=key,
        )

    def _test_permissions_once(self, *, resource_name: str, permissions: list[str], debug: bool = False) -> list[str]:
        _ = debug
        return call_test_iam_permissions(
            client=self.client,
            resource_name=resource_name,
            permissions=permissions,
            api_name=self.permission_api_name,
            service_label=self.SERVICE_LABEL,
            project_id=getattr(self.session, "project_id", None),
            not_found_label=resource_name,
            return_not_enabled=False,
        )

    @property
    def permission_api_name(self) -> str:
        raise NotImplementedError

    def test_iam_permissions(
        self,
        *,
        resource_name: str,
        permissions: list[str],
        debug: bool = False,
        batch_size: int = DEFAULT_TEST_IAM_BATCH_SIZE,
        failed_permissions: dict[str, dict[str, dict[str, Any]]] | None = None,
        show_permission_progress: bool = False,
        failed_permission_writer: FailedPermissionReportWriter | None = None,
    ) -> list[str]:
        permissions = [
            str(permission or "").strip()
            for permission in permissions or []
            if str(permission or "").strip() and str(permission or "").strip() not in self._invalid_permissions
        ]
        if not permissions:
            return []
        normalized_batch_size = max(1, int(batch_size or DEFAULT_TEST_IAM_BATCH_SIZE))
        record_failed_mode = failed_permissions is not None
        if len(permissions) <= normalized_batch_size:
            if show_permission_progress:
                if normalized_batch_size == 1:
                    print(f"[*] Testing permission 1/1 for {resource_name}: {permissions[0]}")
                else:
                    print(
                        f"[*] Testing permissions 1-{len(permissions)}/{len(permissions)} "
                        f"for {resource_name}: {', '.join(permissions)}"
                    )
            for attempt in range(1, RESOURCE_MANAGER_RATE_LIMIT_RETRY_ATTEMPTS + 1):
                try:
                    request = iam_policy_pb2.TestIamPermissionsRequest(resource=resource_name, permissions=permissions)
                    response = self._request(lambda: self.client.test_iam_permissions(request=request))
                    return list(getattr(response, "permissions", []) or [])
                except Exception as exc:
                    if _is_rate_limit_error(exc) and attempt < RESOURCE_MANAGER_RATE_LIMIT_RETRY_ATTEMPTS:
                        pause_seconds = (
                            RESOURCE_MANAGER_FAILED_PERMISSION_WINDOW_SECONDS
                            if record_failed_mode
                            else RESOURCE_MANAGER_BATCH_WINDOW_SECONDS
                        )
                        label = permissions[0] if len(permissions) == 1 and permissions else f"batch {permissions[:3]}"
                        print(
                            f"[*] Hit Resource Manager TestIamPermissions quota for {resource_name} "
                            f"while checking {label}; pausing {pause_seconds}s before retry {attempt + 1}/"
                            f"{RESOURCE_MANAGER_RATE_LIMIT_RETRY_ATTEMPTS}"
                        )
                        time.sleep(pause_seconds)
                        continue
                    if _is_rate_limit_error(exc):
                        result = handle_service_error(
                            exc,
                            api_name=self.permission_api_name,
                            resource_name=resource_name,
                            service_label=self.SERVICE_LABEL,
                            project_id=getattr(self.session, "project_id", None),
                            not_found_label=resource_name,
                            return_not_enabled=False,
                        )
                        return [] if result in (None, "Not Enabled") else list(result or [])

                    error_text = str(exc)
                    if "is not valid for this resource" in error_text:
                        invalid_permissions = self._register_invalid_permissions(error_text, permissions)
                        if failed_permissions is not None and invalid_permissions:
                            record_failed_permissions(
                                failed_permissions,
                                resource_type=self.RESOURCE_TYPE,
                                resource_name=resource_name,
                                permissions=sorted(invalid_permissions),
                                error=error_text,
                                report_writer=failed_permission_writer,
                            )
                        if len(permissions) > 1:
                            filtered_permissions = [permission for permission in permissions if permission not in invalid_permissions]
                            if filtered_permissions and len(filtered_permissions) != len(permissions):
                                return self.test_iam_permissions(
                                    resource_name=resource_name,
                                    permissions=filtered_permissions,
                                    debug=debug,
                                    batch_size=min(normalized_batch_size, len(filtered_permissions)),
                                    failed_permissions=failed_permissions,
                                    show_permission_progress=False,
                                    failed_permission_writer=failed_permission_writer,
                                )
                        return []
                    if record_failed_mode and normalized_batch_size > 1:
                        print(
                            f"[*] TestIamPermissions batch failed for {resource_name}; "
                            f"retrying the {len(permissions)} permissions individually to isolate errors"
                        )
                        granted: set[str] = set()
                        for permission in permissions:
                            granted.update(
                                self.test_iam_permissions(
                                    resource_name=resource_name,
                                    permissions=[permission],
                                    debug=debug,
                                    batch_size=1,
                                    failed_permissions=failed_permissions,
                                    show_permission_progress=False,
                                    failed_permission_writer=failed_permission_writer,
                                )
                            )
                        return sorted(granted)
                    if failed_permissions is not None:
                        record_failed_permissions(
                            failed_permissions,
                            resource_type=self.RESOURCE_TYPE,
                            resource_name=resource_name,
                            permissions=permissions,
                            error=error_text,
                            report_writer=failed_permission_writer,
                        )
                        if "is not valid for this resource" in error_text:
                            return []
                    result = handle_service_error(
                        exc,
                        api_name=self.permission_api_name,
                        resource_name=resource_name,
                        service_label=self.SERVICE_LABEL,
                        project_id=getattr(self.session, "project_id", None),
                        not_found_label=resource_name,
                        return_not_enabled=False,
                    )
                    return [] if result in (None, "Not Enabled") else list(result or [])

        granted: set[str] = set()
        batches = [
            permissions[index:index + normalized_batch_size]
            for index in range(0, len(permissions), normalized_batch_size)
        ]
        total_batches = len(batches)
        if record_failed_mode:
            throttle_calls_per_window = RESOURCE_MANAGER_FAILED_PERMISSION_CALLS_PER_WINDOW
            throttle_window_seconds = RESOURCE_MANAGER_FAILED_PERMISSION_WINDOW_SECONDS
        else:
            throttle_calls_per_window = RESOURCE_MANAGER_BATCH_CALLS_PER_WINDOW
            throttle_window_seconds = RESOURCE_MANAGER_BATCH_WINDOW_SECONDS
        rate_calls_per_min = (throttle_calls_per_window * 60.0) / float(throttle_window_seconds)
        for index, batch in enumerate(batches, start=1):
            if show_permission_progress and batch:
                start_index = ((index - 1) * normalized_batch_size) + 1
                end_index = min(index * normalized_batch_size, len(permissions))
                if normalized_batch_size == 1:
                    print(f"[*] Testing permission {index}/{total_batches} for {resource_name}: {batch[0]}")
                else:
                    print(
                        f"[*] Testing permissions {start_index}-{end_index}/{len(permissions)} "
                        f"for {resource_name}: {', '.join(batch)}"
                    )
            granted.update(
                self.test_iam_permissions(
                    resource_name=resource_name,
                    permissions=batch,
                    debug=debug,
                    batch_size=normalized_batch_size,
                    failed_permissions=failed_permissions,
                    show_permission_progress=False,
                    failed_permission_writer=failed_permission_writer,
                )
            )
            if record_failed_mode:
                if index % RESOURCE_MANAGER_FAILED_PERMISSION_CALLS_PER_WINDOW == 0 and index < total_batches:
                    remaining_batches = total_batches - index
                    pause_windows_remaining = (
                        remaining_batches + RESOURCE_MANAGER_FAILED_PERMISSION_CALLS_PER_WINDOW - 1
                    ) // RESOURCE_MANAGER_FAILED_PERMISSION_CALLS_PER_WINDOW
                    approx_minutes_remaining = (
                        pause_windows_remaining * RESOURCE_MANAGER_FAILED_PERMISSION_WINDOW_SECONDS
                    ) / 60.0
                    print(
                        f"[*] Completed {index}/{len(batches)} record-failed TestIamPermissions calls for "
                        f"{resource_name}; pausing {RESOURCE_MANAGER_FAILED_PERMISSION_WINDOW_SECONDS}s "
                        f"(batch_size={normalized_batch_size}, rate≈{rate_calls_per_min:.2f} calls/min, "
                        f"ETA≈{approx_minutes_remaining:.1f} min)"
                    )
                    time.sleep(RESOURCE_MANAGER_FAILED_PERMISSION_WINDOW_SECONDS)
            elif index % RESOURCE_MANAGER_BATCH_CALLS_PER_WINDOW == 0 and index < total_batches:
                remaining_batches = total_batches - index
                pause_windows_remaining = (
                    remaining_batches + RESOURCE_MANAGER_BATCH_CALLS_PER_WINDOW - 1
                ) // RESOURCE_MANAGER_BATCH_CALLS_PER_WINDOW
                approx_minutes_remaining = (pause_windows_remaining * RESOURCE_MANAGER_BATCH_WINDOW_SECONDS) / 60.0
                print(
                    f"[*] Completed {index}/{len(batches)} TestIamPermissions batches for {resource_name}; "
                    f"pausing {RESOURCE_MANAGER_BATCH_WINDOW_SECONDS}s "
                    f"(batch_size={normalized_batch_size}, rate≈{rate_calls_per_min:.2f} calls/min, "
                    f"ETA≈{approx_minutes_remaining:.1f} min)"
                )
                time.sleep(RESOURCE_MANAGER_BATCH_WINDOW_SECONDS)
        return sorted(granted)

