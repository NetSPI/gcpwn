def _permission_file(filename: str) -> str:
    return module_data_file(__file__, "data", filename)


def _scripts_output_dir() -> Path:
    return Path(__file__).resolve().parents[4] / "scripts"


def _is_rate_limit_error(exc: Exception) -> bool:
    message = str(exc or "")
    markers = (
        "429 Quota exceeded",
        "RATE_LIMIT_EXCEEDED",
        "Too Many Requests",
        "quota metric",
    )
    return any(marker in message for marker in markers)


def _resource_state_label(value: Any) -> str:
    token = str(value or "").strip().upper()
    if token in {"", "NONE"}:
        return "UNKNOWN_STATE"
    if token in {"0", "STATE_UNSPECIFIED"}:
        return "STATE_UNSPECIFIED"
    if token in {"1", "ALIVE", "ACTIVE"}:
        return "ACTIVE"
    if token in {"2", "DELETE_REQUESTED"}:
        return "DELETE_REQUESTED"
    return str(value or "").strip() or "UNKNOWN_STATE"


def _persist_metadata_gcp(session, data_object: Any, resource_type: str) -> None:
    def _extras(_obj: Any, raw: dict[str, Any]) -> dict[str, Any]:
        if resource_type == "organization":
            return {"parent": "None", "project_id": "N/A", "type": "org"}
        if resource_type == "project":
            return {
                "parent": str(raw.get("parent") or ""),
                "project_id": str(raw.get("project_id") or ""),
                "type": "project",
            }
        if resource_type == "folder":
            return {
                "parent": str(raw.get("parent") or ""),
                "project_id": "N/A",
                "type": "folder",
            }
        return {}

    if_column_matches = ["project_id"] if resource_type == "project" else None
    save_to_table(
        session,
        "abstract_tree_hierarchy",
        data_object,
        extra_builder=_extras,
        if_column_matches=if_column_matches,
    )


def _dedupe_rows(rows: Iterable[dict[str, Any]], *, key_field: str = "name") -> list[dict[str, Any]]:
    ordered: list[dict[str, Any]] = []
    seen: set[str] = set()
    for row in rows or []:
        payload = resource_to_dict(row)
        key = str(payload.get(key_field) or "").strip()
        if not key or key in seen:
            continue
        seen.add(key)
        ordered.append(payload)
    return ordered


class FailedPermissionReportWriter:
    def __init__(self, *, output_dir: str | Path | None = None) -> None:
        self.base_dir = Path(output_dir) if output_dir else _scripts_output_dir()
        self.base_dir.mkdir(parents=True, exist_ok=True)
        self._initialized_resource_types: set[str] = set()
        self._seen_permissions: dict[str, set[str]] = {}

    def _debug_path(self, resource_type: str) -> Path:
        return self.base_dir / f"resourcemanager_{resource_type}_failed_permissions.txt"

    def _list_path(self, resource_type: str) -> Path:
        return self.base_dir / f"resourcemanager_{resource_type}_failed_permissions_list.txt"

    def prepare(self, resource_type: str) -> None:
        token = str(resource_type or "").strip()
        if not token or token in self._initialized_resource_types:
            return
        debug_path = self._debug_path(token)
        list_path = self._list_path(token)
        debug_path.write_text(
            "\n".join(
                [
                    "# Generated by enum_resources --record-failed-permissions",
                    f"# resource_type={token}",
                    f"# clean_permission_list={list_path.name}",
                    "# Streaming failures are appended below as they happen.",
                    "",
                    "# Failures",
                    "",
                ]
            )
            + "\n",
            encoding="utf-8",
        )
        list_path.write_text("", encoding="utf-8")
        self._initialized_resource_types.add(token)
        self._seen_permissions[token] = set()

    def record(self, *, resource_type: str, permission: str, resource_name: str, error: str) -> None:
        token = str(resource_type or "").strip()
        permission_token = str(permission or "").strip()
        if not token or not permission_token:
            return

        self.prepare(token)
        debug_path = self._debug_path(token)
        list_path = self._list_path(token)
        sanitized_error = " ".join(str(error or "").split())

        with debug_path.open("a", encoding="utf-8") as handle:
            handle.write(
                " | ".join(
                    part
                    for part in [
                        permission_token,
                        f"resource={resource_name}" if str(resource_name or "").strip() else "",
                        f"error={sanitized_error}" if sanitized_error else "",
                    ]
                    if part
                )
                + "\n"
            )

        seen_permissions = self._seen_permissions.setdefault(token, set())
        if permission_token not in seen_permissions:
            with list_path.open("a", encoding="utf-8") as handle:
                handle.write(permission_token + "\n")
            seen_permissions.add(permission_token)


def record_failed_permissions(
    failed_permissions: dict[str, dict[str, dict[str, Any]]],
    *,
    resource_type: str,
    resource_name: str,
    permissions: Iterable[str],
    error: str,
    report_writer: FailedPermissionReportWriter | None = None,
) -> None:
    resource_type_token = str(resource_type or "").strip()
    bucket = failed_permissions.setdefault(resource_type_token, {})
    for permission in permissions or []:
        token = str(permission or "").strip()
        if not token:
            continue
        entry = bucket.setdefault(token, {"count": 0, "resources": set(), "errors": set()})
        entry["count"] = int(entry.get("count", 0)) + 1
        if resource_name:
            entry.setdefault("resources", set()).add(resource_name)
        if error:
            entry.setdefault("errors", set()).add(error)
        if report_writer is not None:
            report_writer.record(
                resource_type=resource_type_token,
                permission=token,
                resource_name=resource_name,
                error=error,
            )


def merge_failed_permissions(
    destination: dict[str, dict[str, dict[str, Any]]],
    source: dict[str, dict[str, dict[str, Any]]],
) -> dict[str, dict[str, dict[str, Any]]]:
    for resource_type, permission_map in (source or {}).items():
        target_bucket = destination.setdefault(str(resource_type or "").strip(), {})
        for permission, details in (permission_map or {}).items():
            token = str(permission or "").strip()
            if not token:
                continue
            entry = target_bucket.setdefault(token, {"count": 0, "resources": set(), "errors": set()})
            entry["count"] = int(entry.get("count", 0)) + int((details or {}).get("count", 0))
            entry.setdefault("resources", set()).update((details or {}).get("resources", set()) or set())
            entry.setdefault("errors", set()).update((details or {}).get("errors", set()) or set())
    return destination


def write_failed_permission_reports(
    failed_permissions: dict[str, dict[str, dict[str, Any]]],
    *,
    output_dir: str | Path | None = None,
) -> dict[str, str]:
    base_dir = Path(output_dir) if output_dir else _scripts_output_dir()
    base_dir.mkdir(parents=True, exist_ok=True)

    written_paths: dict[str, str] = {}
    for resource_type in sorted(failed_permissions.keys()):
        bucket = failed_permissions.get(resource_type) or {}
        if not bucket:
            continue
        path = base_dir / f"resourcemanager_{resource_type}_failed_permissions.txt"
        list_path = base_dir / f"resourcemanager_{resource_type}_failed_permissions_list.txt"

        list_path.write_text("\n".join(sorted(bucket.keys())) + "\n", encoding="utf-8")

        if path.exists():
            existing_lines = path.read_text(encoding="utf-8").rstrip("\n").splitlines()
        else:
            existing_lines = [
                "# Generated by enum_resources --record-failed-permissions",
                f"# resource_type={resource_type}",
                f"# clean_permission_list={list_path.name}",
                "",
                "# Failures",
                "",
            ]

        summary_lines = [
            "",
            "# Summary",
            f"# unique_failed_permissions={len(bucket)}",
            "",
        ]
        summary_lines.extend(sorted(bucket.keys()))
        summary_lines.extend(["", "# Details"])
        for permission in sorted(bucket.keys()):
            details = bucket.get(permission) or {}
            count = int(details.get("count", 0))
            resources = sorted(str(item) for item in details.get("resources", set()) if str(item).strip())
            errors = sorted(str(item) for item in details.get("errors", set()) if str(item).strip())
            summary_lines.append(
                "# "
                + " | ".join(
                    part
                    for part in [
                        permission,
                        f"count={count}",
                        f"resources={','.join(resources)}" if resources else "",
                        f"errors={'; '.join(errors)}" if errors else "",
                    ]
                    if part
                )
            )
        path.write_text("\n".join(existing_lines + summary_lines) + "\n", encoding="utf-8")
        written_paths[resource_type] = str(path)
    return written_paths


def _add_iam_member(
    *,
    resource_client,
    resource_name: str,
    member: str,
    action_dict: dict[str, Any],
    brute: bool,
    role: str | None,
    debug: bool,
    get_policy,
    set_policy,
    action_scope_key: str,
    get_permission: str,
    set_permission: str,
    resource_label: str,
):
    policy_dict: dict[str, Any] = {}
    additional_binding = {"role": role, "members": [member]}

    if brute:
        print(f"[*] Overwriting {resource_name} to just be {member}")
        policy_dict["bindings"] = [additional_binding]
        policy_dict["version"] = 1
        policy = policy_dict
    else:
        print(f"[*] Fetching current policy for {resource_name}...")
        policy = get_policy(resource_client, resource_name, debug=debug)
        if not policy:
            print(
                f"{UtilityTools.RED}[X] Exiting because the current policy could not be retrieved. "
                f"Supply --overwrite to replace the entire {resource_label} IAM policy if needed."
                f"{UtilityTools.RESET}"
            )
            return -1
        if policy == 404:
            print(
                f"{UtilityTools.RED}[X] Exiting because {resource_name} does not exist. Double check the name."
                f"{UtilityTools.RESET}"
            )
            return -1

        action_dict.setdefault(action_scope_key, {}).setdefault(resource_name, set()).add(get_permission)
        policy_dict["bindings"] = list(policy.bindings)
        policy_dict["bindings"].append(additional_binding)
        policy_dict["etag"] = policy.etag
        policy_dict["version"] = policy.version
        policy = policy_dict

    print(f"[*] New policy below being added to {resource_name}\n{policy_dict.get('bindings', [])}")
    status = set_policy(resource_client, resource_name, policy, debug=debug)
    if not status:
        return status
    if status == 404:
        print(
            f"{UtilityTools.RED}[X] Exiting because {resource_name} does not exist. Double check the name."
            f"{UtilityTools.RESET}"
        )
        return -1

    action_dict.setdefault(action_scope_key, {}).setdefault(resource_name, set()).add(set_permission)
    return status


